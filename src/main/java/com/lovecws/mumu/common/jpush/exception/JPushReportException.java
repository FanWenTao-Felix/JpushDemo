package com.lovecws.mumu.common.jpush.exception;

/**
 * 推送数据统计出现异常
 * @author ganliang
 */
public class JPushReportException extends Exception {

	private static final long serialVersionUID = -398457421021748850L;

	public JPushReportException() {
		super();
	}

	public JPushReportException(String e, Throwable arg1) {
		super(e, arg1);
	}

	public JPushReportException(String e) {
		super();
	}

	public JPushReportException(Throwable e) {
		super();
	}

}
